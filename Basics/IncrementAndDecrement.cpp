#include <bits/stdc++.h>
using namespace std;

int main()
{
   //Post Increment
   int a=4;
   int i=a++;
   cout<<i<<endl;
   cout<<a<<endl;
   cout<<endl;

   //Pre Increment
   int b=4;
   int j=++b;
   cout<<j<<endl;
   cout<<b<<endl;
   cout<<endl;
   
   //Post Decrement
   int c=4;
   int k=c--;
   cout<<k<<endl;
   cout<<c<<endl;
   cout<<endl;
   
   //Pre Decrement
   int d=4;
   int l=--d;
   cout<<l<<endl;
   cout<<d<<endl;
   cout<<endl;
    return 0;
}